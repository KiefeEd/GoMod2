package goaritmatika

import "fmt"

func Penjumlahan(x,y int16) {
	fmt.Println("Hasil dari penjumlahan dari %d + %d adalah", x + y)
}

func Perkalian(x,y int16) {
	fmt.Println("Hasil dari perkalian %d * %d adalah", x*y)
}

func Pembagian(x,y int16) {
	fmt.Println("Hasil dari pembagian %d * %d adalah", x/y)
}